const $ = (selector, ctx = document) => ctx.querySelector(selector);
const $$ = (selector, ctx = document) => ctx.querySelectorAll(selector);
const onReady = (fn) => {
  if (document.readyState !== 'loading') fn();
  else document.addEventListener('DOMContentLoaded', fn);
};

/* ============================================================
   2. NAVBAR — Scroll + Hamburger
============================================================ */

const initNavbar = () => {
  const navbar    = $('#navbar');
  const hamburger = $('#hamburger');
  const navLinks  = $('#navLinks');

  if (!navbar) return;

  const handleScroll = () => {
    if (window.scrollY > 40) {
      navbar.classList.add('scrolled');
    } else {
      navbar.classList.remove('scrolled');
    }
  };

  window.addEventListener('scroll', handleScroll, { passive: true });
  handleScroll();

  if (hamburger && navLinks) {
    hamburger.addEventListener('click', () => {
      const isOpen = hamburger.classList.toggle('open');
      navLinks.classList.toggle('open', isOpen);
      hamburger.setAttribute('aria-expanded', String(isOpen));
      document.body.style.overflow = isOpen ? 'hidden' : '';
    });

    $$('.nav-link', navLinks).forEach((link) => {
      link.addEventListener('click', () => {
        hamburger.classList.remove('open');
        navLinks.classList.remove('open');
        hamburger.setAttribute('aria-expanded', 'false');
        document.body.style.overflow = '';
      });
    });
  }
};

/* ============================================================
   3. ACORDEÓN — Quiénes Somos
============================================================ */

const initAccordion = () => {
  const accordion = $('#accordion');
  if (!accordion) return;

  const items = $$('.accordion__item', accordion);

  items.forEach((item) => {
    const trigger = $('.accordion__trigger', item);
    if (!trigger) return;

    trigger.addEventListener('click', () => {
      const isOpen = item.classList.contains('is-open');

      items.forEach((otherItem) => {
        if (otherItem !== item) closeAccordionItem(otherItem);
      });

      if (isOpen) {
        closeAccordionItem(item);
      } else {
        openAccordionItem(item);
      }
    });
  });
};

const openAccordionItem = (item) => {
  const trigger = $('.accordion__trigger', item);
  const panel   = $('.accordion__panel', item);
  const body    = $('.accordion__body', item);

  if (!trigger || !panel || !body) return;

  item.classList.add('is-open');
  trigger.setAttribute('aria-expanded', 'true');
  panel.removeAttribute('hidden');
  panel.style.maxHeight = body.scrollHeight + 'px';
};

const closeAccordionItem = (item) => {
  const trigger = $('.accordion__trigger', item);
  const panel   = $('.accordion__panel', item);

  if (!trigger || !panel) return;

  item.classList.remove('is-open');
  trigger.setAttribute('aria-expanded', 'false');
  panel.style.maxHeight = '0';
};

